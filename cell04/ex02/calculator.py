#!/usr/bin/env python

fnum = int(input("Give me the first number: "))
snum = int(input("Give me the second number: "))

print("Thank you!")

print(str(fnum) + " + " + str(snum) + " = " + str(fnum+snum))
print(str(fnum) + " - " + str(snum) + " = " + str(fnum-snum))
print(str(fnum) + " / " + str(snum) + " = " + str(int(fnum/snum)))
print(str(fnum) + " * " + str(snum) + " = " + str(int(fnum*snum)))